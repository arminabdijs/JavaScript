"use strict";
let num = 1_000_000;
console.log(num); // 1000000

// --------------------------------------------------------------------------------------

let price = 99_999.99;
console.log(price); // 99999.99

// --------------------------------------------------------------------------------------

//0b means binary number (2 is the base)
let binaryNum = 0b1010_1010;
console.log(binaryNum); // 170

// --------------------------------------------------------------------------------------

//0x means hexadecimal number (16 is the base)
let hexNum = 0xFF_FF_FF;
console.log(hexNum); // 16777215

// --------------------------------------------------------------------------------------

//0o means octal number (8 is the base)
let octalNum = 0o12_34567;
console.log(octalNum); // 342391

